mood = input("What is your mood today? ")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Cheer up, mate!")
elif mood == "excited":
    print("Let's celebrate!")
elif mood == "relaxed":
    print("It is great to see you relaxed!")
else:
    print("I don't recognize this mood")
